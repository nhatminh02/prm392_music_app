package com.example.prm392_musicapp;

public class LibFunc {
    private String des;
    private String btn;

    public LibFunc(String des, String btn) {
        this.des = des;
        this.btn = btn;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }

    public String getBtn() {
        return btn;
    }

    public void setBtn(String btn) {
        this.btn = btn;
    }
}
